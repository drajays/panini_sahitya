def main():
    with open(BHAG_JSON, 'r', encoding='utf-8') as f:
        bhag = json.load(f)
    
    updated_count = 0
    for entry in bhag['data']:
        if entry.get('c') == '6.1' and str(entry.get('n')) in verses_data:
            num = str(entry.get('n'))
            vdata = verses_data[num]
            entry['anv'] = vdata['anv']
            entry['hs'] = vdata['hs']
            entry['pc'] = vdata['pc']
            updated_count += 1
            print(f"Updated bhagavatam.json verse 6.1.{num}")
    
    with open(BHAG_JSON, 'w', encoding='utf-8') as f:
        json.dump(bhag, f, ensure_ascii=False, separators=(',', ':'))
    print(f"Saved {BHAG_JSON} ({updated_count} verses updated).")

    with open(SKANDHA_6_JSON, 'r', encoding='utf-8') as f:
        sk6 = json.load(f)
    
    for num, vdata in verses_data.items():
        key = f"6.1|{num}"
        sk6[key] = vdata['sk_rows']
        print(f"Updated skandha-6.json key {key}")
        
    with open(SKANDHA_6_JSON, 'w', encoding='utf-8') as f:
        json.dump(sk6, f, ensure_ascii=False, separators=(',', ':'))
    print(f"Saved {SKANDHA_6_JSON}.")

if __name__ == '__main__':
    main()
