CORPUS_RE_FIXES = [
    # avagraha misread as digit ५ after ो/े (e.g. यन्मनो५नुविधीयते)
    (re.compile(r'([ोे]) ?५(?=[अ-ह])'), r'\1ऽ'),
    (re.compile(r'अग्रि(?!म)'), 'अग्नि'),
]


def apply_fixes(text, fixes, regexes=()):
    for a, b in fixes:
        text = text.replace(a, b)
    for rx, b in regexes:
        text = rx.sub(b, text)
    # collapse doubled spaces that replacements may create
    text = re.sub(r'[ \t]+', ' ', text)
    text = re.sub(r'\n ', '\n', text)
    text = re.sub(r'\n{2,}', '\n', text)
    return text


def main():
    sbfile = REPO / 'data' / 'shankarbhashya.json'
    sb = json.load(open(sbfile))

    for ch, verses in sb.items():
        chfixes = []
        f = FIXDIR / f'ch{int(ch):02d}.json'
        if f.exists():
            chfixes = json.load(open(f))
        for n in list(verses):
            t = apply_fixes(verses[n], CORPUS_FIXES, CORPUS_RE_FIXES)
            t = apply_fixes(t, chfixes)
            verses[n] = t.strip()

    json.dump(sb, open(sbfile, 'w'), ensure_ascii=False, indent=1)

    gfile = REPO / 'data' / 'gita.json'
    g = json.load(open(gfile))
    updated = 0
    for v in g['data']:
        c, n = v.get('c'), str(v.get('n'))
        if c in sb and n in sb[c]:
            v['sb'] = sb[c][n]
            updated += 1
    json.dump(g, open(gfile, 'w'), ensure_ascii=False, separators=(',', ':'))
    print('updated sb for', updated, 'verses')


if __name__ == '__main__':
    main()
